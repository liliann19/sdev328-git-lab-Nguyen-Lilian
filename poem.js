const poem = `
    Behold the coder, with weary eyes,
    Fueled by caffeine and pizza pies.
    At 3 A.M., they chase a dream,
    Debugging life with zero esteem.

    "Just one more line," they boldly claim,
    Until the bug hunt turns to shame.
    For every fix, two more will rise,
    Like Hydra heads, to their surprise.

    The product owner wields their might:
    "Can we ship this... by tonight?"
    "Of course," we say, with hollow grin,
    (Though dead inside, we're cold within).

    Designers dream of perfect spec,
    But devs just mutter, "What the heck?"
    "Pixel-perfect? Sure, we'll try,"
    (But CSS will make us cry).

    Agile boards, with endless cards,
    Make sprinting feel like life behind bars.
    "Move it to Done," the scrum master cheers,
    Yet technical debt draws closer, near.

    Stack Overflow, our saving grace,
    With snippets stolen—cut, then paste.
    We offer prayers to gods of code,
    In hopes the CI won't explode.

    Docker containers drift like ghosts,
    While Jenkins breaks and haunts our posts.
    The cloud is down? Oh, what fresh hell!
    "Just restart it," we know too well.

    And though we grumble, curse, and sigh,
    We live to build, to ship, to try.
    For deep inside, beneath the mess,
    We love this chaos—more or less.

    So here's to devs, and every quirk,
    Who brave the night and make it work.
    We'll meet again at dawn's first ray,
    With coffee brewed and bugs to slay.
`;

